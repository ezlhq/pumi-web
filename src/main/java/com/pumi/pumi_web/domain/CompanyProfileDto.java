package com.pumi.pumi_web.domain;

public class CompanyProfileDto {

	private String id         ;
	private String shot_name  ;
	private String email      ;
	private String phone      ;
	private String tele_phone ;
	private String fax        ;
	private String address    ;
	private String text_info  ;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getShot_name() {
		return shot_name;
	}
	public void setShot_name(String shot_name) {
		this.shot_name = shot_name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getTele_phone() {
		return tele_phone;
	}
	public void setTele_phone(String tele_phone) {
		this.tele_phone = tele_phone;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getText_info() {
		return text_info;
	}
	public void setText_info(String text_info) {
		this.text_info = text_info;
	}

	
}
